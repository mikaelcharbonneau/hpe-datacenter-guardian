// Helper functions
export const getDatacenterById = (id: string): Datacenter | undefined => {
  return locationData.find(dc => dc.id === id);
};

export const getDataHallsByDatacenter = (datacenterId: string): DataHall[] => {
  const datacenter = getDatacenterById(datacenterId);
  return datacenter ? datacenter.dataHalls : [];
};

export const getCabinetsByDataHall = (datacenterId: string, dataHallId: string): Cabinet[] => {
  const datacenter = getDatacenterById(datacenterId);
  if (!datacenter) return [];
  
  const dataHall = datacenter.dataHalls.find(dh => dh.id === dataHallId);
  return dataHall ? dataHall.cabinets : [];
};

export const getLocationDisplayName = (datacenterId: string, dataHallId?: string): string => {
  const datacenter = getDatacenterById(datacenterId);
  if (!datacenter) return '';
  
  if (!dataHallId) return datacenter.name;
  
  const dataHall = datacenter.dataHalls.find(dh => dh.id === dataHallId);
  return dataHall ? `${datacenter.name} / ${dataHall.name}` : datacenter.name;
};